    randomChoice(options) {
        const total = options.reduce((sum, opt) => sum + opt.chance, 0);
        let rand = Math.random() * total;
        for (const opt of options) {
            if (rand < opt.chance) return opt.scene;
            rand -= opt.chance;
        }
        return options[0].scene;
    }

    checkRequirement(req) {
        if (!req) return true;
        if (req.type && req.value !== undefined) {
            return this.state[req.type] >= req.value;
        }
        return false;
    }

