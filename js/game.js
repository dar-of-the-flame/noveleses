    showChoices() {
        this.continueIndicator.classList.remove('visible');
        if (this.mobileSwipeIndicator) {
            this.mobileSwipeIndicator.classList.remove('visible');
        }
        
        this.choicesContainer.innerHTML = '';
        
        if (!this.currentChoices || this.currentChoices.length === 0) return;
        
        this.currentChoices.forEach((choice, index) => {
            const button = document.createElement('button');
            button.className = 'choice-btn';
            
            let choiceText = choice.text;
            if (choice.requires) {
                const meetsRequirement = this.checkRequirement(choice.requires);
                if (!meetsRequirement && choice.failure) {
                    choiceText = `üîí ${choiceText}`;
                }
            }
            
            // –£–±–∏—Ä–∞–µ–º –Ω—É–º–µ—Ä–∞—Ü–∏—é –¥–ª—è –æ–¥–∏–Ω–æ—á–Ω—ã—Ö –≤—ã–±–æ—Ä–æ–≤
            if (this.currentChoices.length > 1) {
                button.innerHTML = `<span class="choice-number">${index + 1}</span>${choiceText}`;
            } else {
                button.innerHTML = choiceText;
            }
            
            if (choice.requires && !this.checkRequirement(choice.requires) && !choice.failure) {
                button.classList.add('disabled');
                button.title = "–¢—Ä–µ–±–æ–≤–∞–Ω–∏—è –Ω–µ –≤—ã–ø–æ–ª–Ω–µ–Ω—ã";
            } else {
                button.addEventListener('click', () => { 
                    this.makeChoice(choice); 
                });
            }
            
            this.choicesContainer.appendChild(button);
        });
        
        setTimeout(() => {
            this.choicesContainer.classList.add('visible');
        }, 100);
    }

