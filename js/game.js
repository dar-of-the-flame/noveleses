    async makeChoice(choice) {
        console.log('Making choice:', choice.text);
        
        this.state.choicesMade++;
        
        if (choice.effects) {
            this.applyEffects(choice.effects);
        }
        
        if (choice.flags) {
            choice.flags.forEach(flag => {
                if (!this.state.flags.includes(flag)) {
                    this.state.flags.push(flag);
                }
            });
        }
        
        let nextScene = choice.nextScene;
        
        if (choice.requires) {
            if (this.checkRequirement(choice.requires)) {
                nextScene = choice.success ? choice.success.nextScene : choice.nextScene;
                if (choice.success && choice.success.text) {
                    await this.showIntermediateText(choice.success.text);
                }
                if (choice.success && choice.success.effects) {
                    this.applyEffects(choice.success.effects);
                }
                if (choice.success && choice.success.flags) {
                    choice.success.flags.forEach(flag => {
                        if (!this.state.flags.includes(flag)) {
                            this.state.flags.push(flag);
                        }
                    });
                }
            } else {
                nextScene = choice.failure ? choice.failure.nextScene : choice.nextScene;
                if (choice.failure && choice.failure.text) {
                    await this.showIntermediateText(choice.failure.text);
                }
            }
        }
        
        await this.loadScene(nextScene);
    }

